extension StopViewerModel {
    /// Depart item.
    /// - Parameters:
    ///   - DepartItem: Parameter description
    var departItem: DepartItem {
        return DepartItem(patternId:self.pattern.id ?? "", transportNumber: "", toStation: pattern.desc ?? "", departTimes: items)
    }
    /// Items.
    /// - Parameters:
    ///   - [DepartureItem]: Parameter description
    var items: [DepartureItem] {
        return times.map({ DepartureItem(time: $0.departureDate.timeIntervalSince1970, item: $0) })
    }
    
    /// Route.
    /// - Parameters:
    ///   - String: Parameter description
    var route: String {
        let desc = pattern.desc ?? ""
        return desc.components(separatedBy: " ").first ?? ""
    }
    
    /// To string.
    /// - Parameters:
    ///   - String: Parameter description
    var toString: String {
        let desc = pattern.desc ?? ""
        return desc.slice(from: "to", to: "(") ?? ""
    }
    
    
    /// Schedule.
    /// - Parameters:
    ///   - [StopScheduleModel]: Parameter description
    var schedule: [StopScheduleModel] {
        return times.map({
            return StopScheduleModel(time: $0, route: route, to: $0.headsign ?? toString)
        })
    }
    
    /// Schedule items.
    /// - Parameters:
    ///   - [StopScheduleItem]: Parameter description
    var scheduleItems: [StopScheduleItem] {
        return times.map({
            let time = $0.departureDate.timeForStopViewer()
            return StopScheduleItem(leftText: route, middleText: toString, rightText: time)
        })
    }
    
    /// Schedule item.
    /// - Parameters:
    ///   - StopScheduleItem: Parameter description
    var scheduleItem: StopScheduleItem {
        return StopScheduleItem(leftText: "", middleText: "", rightText: "")
    }
    
    /// Top schedule item.
    /// - Parameters:
    ///   - StopScheduleItem: Parameter description
    static var topScheduleItem: StopScheduleItem {
        return StopScheduleItem(leftText: "Route".localized(), middleText: "To".localized(), rightText: "Departure".localized())
