    /// Start timer
    /// Starts timer.
    func startTimer() {
        timer = Timer.scheduledTimer(withTimeInterval: 10.0, repeats: true) { timer in
            // This closure will be called every 10 seconds
            rotationAngle += .degrees(360)
            stopViewer.getGraphQLStopTimes()
        }
    }
    
    /// Stop timer
    /// Stops timer.
    func stopTimer() {
        timer?.invalidate()
        timer = nil
    }
    
    
