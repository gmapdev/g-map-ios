struct DepartItemView: View {
    @ObservedObject var stopViewerViewModel = StopViewerViewModel.shared
    @ObservedObject var mapManager = MapManager.shared
    @State var routeManager = RouteManager.shared
    @Binding var item: GraphQLDepartItem
    
    /// Body.
    /// - Parameters:
    ///   - some: Parameter description
    var body: some View {
        return VStack {
            if AccessibilityManager.shared.pubIsLargeFontSize {
                topViewAODA(isSelected: item.isExpanded)
            } else {
                topView(isSelected: item.isExpanded)
            }
            Divider()
                .background(Color.black)
            if item.isExpanded {
                departuresView
            }
        }
        .background(Color.white)
        .padding(15)
    }
    
    /// Icon for agency
    /// - Returns: UIImage?
    /// Icon for agency.
    func iconForAgency() -> UIImage?{
        let agencyName = item.pattern.route.agency.name
        let mappedAgency = Helper.shared.mapAgencyNameAliase(agencyName: agencyName)
        var agencyImage: UIImage? = nil
        agencyImage = RouteViewerModel.shared.agencyLogos[mappedAgency.lowercased()]
        return agencyImage
    }
    
    func deparureTopButtonAction() {
        item.isExpanded.toggle()
        if item.isExpanded {
            let routeId = item.routeId()
            stopViewerViewModel.pubSelectedDepartItem = item
            routeManager.presentBusRoute(id: routeId)
        }else{
            stopViewerViewModel.pubSelectedDepartItem = nil
            mapManager.forceCleanMapReDrawRoute()
            DispatchQueue.main.asyncAfter(deadline: .now() + 0.5) {
                mapManager.reCenterRouteBounds()
            }
        }
        if let selectedStop = StopViewerViewModel.shared.selectedStop{
            let stop = selectedStop
            let lat = stop.lat
            let lon = stop.lon
            let coordinates = CLLocationCoordinate2D(latitude: lat, longitude: lon)
            mapManager.removeStopMarker()
            mapManager.addStopMarker(coordinates: coordinates)
        }
    }
    
    /// Top view.
    /// - Parameters:
    ///   - isSelected: Parameter description
    /// - Returns: some View
    func topView(isSelected: Bool) -> some View {
        let agencyImage = iconForAgency()
        return HStack {
                if let agencyImage = agencyImage {
                    Image(uiImage: agencyImage)
                        .resizable().aspectRatio(contentMode: .fit)
                        .frame(width: 50, height: 50, alignment: .center)
                        .padding(.bottom, 5)
                }
            
            TextLabel(stopViewerViewModel.graphQLExtractHeadsignFromPattern(item: item))
                .font(isSelected ? .body.bold() : .body)
                .foregroundColor(.black)
                .padding(.leading, 5)
            
            Spacer()
            if let departTime = item.departTimes.first {
                graphQLTimeView(departTime: departTime, isSelected: item.isExpanded, isFormatted: true)
                    .frame(width: 80, alignment: .top)
            }
            Button(action: {
                deparureTopButtonAction()
            }) {
                Image(item.isExpanded ? "expand_up_icon" : "expand_down_icon")
                    .resizable()
                    .frame(width: 25, height: 25)
            }
            .frame(width: 40, alignment: .center)
            .accessibilityElement(children: .ignore)
            .addAccessibility(text: item.isExpanded ? AvailableAccessibilityItem.departureCollapsButton.rawValue : AvailableAccessibilityItem.departureExpandButton.rawValue)
            .accessibilityAction {
                deparureTopButtonAction()
            }
            .accessibilityRemoveTraits(.isButton)

        }.padding(.top, 10)
    }
    
    /// Top view a o d a.
    /// - Parameters:
    ///   - isSelected: Parameter description
    /// - Returns: some View
    func topViewAODA(isSelected: Bool) -> some View {
        let agencyImage = iconForAgency()
        return VStack {
                if let agencyImage = agencyImage {
                    HStack {
                        Image(uiImage: agencyImage)
