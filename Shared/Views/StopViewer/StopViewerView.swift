    /// Convert time.
    /// - Parameters:
    ///   - time: Parameter description
    /// - Returns: Date
    func convertTime(time: String) -> Date{
        let inFormatter = DateFormatter()
        let language = SettingsManager.shared.appLanguage
        inFormatter.locale = Locale(identifier: language.languageCode())
        inFormatter.dateFormat = "hh:mm a"
        inFormatter.timeZone = EnvironmentManager.shared.currentTimezone
        let convertedTime = inFormatter.date(from: time) ?? Date()
        return convertedTime
    }
    
    
