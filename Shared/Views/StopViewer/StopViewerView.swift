                        .clipShape(RoundedCorner(radius: 10, corners: [.topLeft, .topRight]))
                }
                .zIndex(8)
                
            }
            .onChange(of: isToggle, perform: { newValue in
                if newValue {
                    rotationAngle += .degrees(360)
                    stopViewer.getGraphQLStopTimes()
                    startTimer()
                } else {
                    stopTimer()
                }
            })
            .onAppear(perform: {
                rotationAngle += .degrees(360)
                if let selectedStop = stopViewer.selectedStop {
                    let stop = selectedStop
                    let lat = stop.lat
                    let lon = stop.lon
                    let coordinates = CLLocationCoordinate2D(latitude: lat, longitude: lon)
                    mapManager.removeStopMarker()
                    mapManager.addStopMarker(coordinates: coordinates)
                    stopViewer.iconForAgencies()
                }
                
                DispatchQueue.main.asyncAfter(deadline: .now() + 0.2) {
                    mapManager.reCenterRouteBounds()
                }
                
                startTimer()
            })
            .onDisappear {
                stopTimer()
                if tabBarMenuManager.currentItemTab != .stops{
                    mapManager.removeStopMarker()
                }
                if mapManager.pubIsInTripPlan{
                    let viewArea = ViewArea(topRight: CGPoint(x:UIScreen.main.bounds.width,y:0), bottomLeft: CGPoint(x:0, y:Helper.shared.getDeafultViewHeight(heightPosition: .middle)))
                    mapManager.setCenterArea(viewArea: viewArea, mapViewHeight: Helper.shared.getDefaultMapViewHeight(), mapViewWidth: UIScreen.main.bounds.width)
                }
            }
            
        }
