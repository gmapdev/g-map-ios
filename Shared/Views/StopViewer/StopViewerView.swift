struct DepartItem: Identifiable {
    let id = UUID()
    let patternId: String
    let transportNumber: String
    let toStation: String
    var departTimes: [DepartureItem]
    var isExpanded: Bool = false
    
    /// Get stop title.
    /// - Parameters:
    ///   - toStation: Parameter description
    /// - Returns: String
    func getStopTitle(toStation: String) -> String{
        var title = toStation
        let regex = "\\(([0-9a-zA-Z,:].*?)\\)"
        let removal = Helper.shared.regexMatches(for: regex, in: toStation)
        for r in removal{
            if let range = title.range(of: r){
                title.removeSubrange(range)
            }
        }
        
        if let fromTextRange = title.range(of: "from"){
            title.removeSubrange(fromTextRange.lowerBound..<title.endIndex)
        }
        
        if title.isEmpty {
            let routeId = self.routeId()
            let routes = RouteViewerModel.shared.pubRouteItems
            let filteredRoute = routes.first(where: {$0.route.id == routeId})
            if let filteredRoute = filteredRoute {
                title = filteredRoute.route.shortName ?? filteredRoute.route.longName ?? filteredRoute.route.busRouteNumber
            }
        }
        return title
    }
    
    /// Extract headsign from pattern.
    /// - Parameters:
    ///   - item: Parameter description
    /// - Returns: String
    func extractHeadsignFromPattern(item: DepartItem) -> String {
        var routeShortName = ""
        var headsign = ""
        
        let routeId = self.routeId()
        let routes = RouteViewerModel.shared.pubRouteItems
        var filteredRoute = routes.first(where: {$0.route.id == routeId})
        if filteredRoute == nil {
            filteredRoute = routes.first(where: {$0.route.id == routeIdV2()})
        }
        if let filteredRoute = filteredRoute {
            routeShortName = filteredRoute.route.shortName ?? filteredRoute.route.longName ?? filteredRoute.route.busRouteNumber
        }
        
        if headsign.isEmpty {
            if let departHeadSign = departTimes[0].item.headsign {
                headsign = departHeadSign
            }
        }
        
        // In case stop time headsign is blank, extract headsign from the pattern 'desc' attribute
        // (format: '49 to <Destination> (<destid>)[ from <Origin> (<originid)]').
        if headsign.isEmpty {
            let desc = toStation
            let matches = Helper.shared.extractSubstring(desc, regex: #"(?: to )(.*?)(?: \()"#)
            if let matches = matches {
                headsign = matches
            }
            
        }
        
        // If that regex didn't work, try the old regex
        if headsign.isEmpty {
            let desc = toStation
            let matches = Helper.shared.extractSubstring(desc, regex: #" to ([^(from)]+) \(.+\)"#)
            if let matches = matches {
                headsign = matches
            }
        }
        /*
           * In some cases the string generated by OTP doesn't include a " "
           * see https://github.com/opentripplanner/OpenTripPlanner/blob/7ce72cf554e6cf469d683869db5684eae3ce86f8/src/main/java/org/opentripplanner/graph_builder/module/TripPatternNamer.java#L72-L183
           * In this case, we need to rely on the routeShortName to determine what to remove
           */
        if headsign.isEmpty {
            let desc = toStation
            let matches = Helper.shared.extractSubstring(desc, regex: #"^[^(]*$"#)
            if let matches = matches {
                headsign = matches.replacingOccurrences(of: "\(routeShortName) ", with: "")
            }
        }

        
        // If the headsign is still blank, show the description
        if headsign.isEmpty{
            headsign = toStation
        }
        
        if headsign.isEmpty && toStation.isEmpty {
            if departTimes.count > 0 {
                if let departHeadSign = departTimes[0].item.headsign {
                    headsign = departHeadSign
                }
            }
        }
        
        if headsign.contains(routeShortName) {
            headsign = headsign.replacingOccurrences(of: routeShortName, with: "")
        }
        
        return "\(routeShortName) \(headsign)"
    }
    
    /// Route id v2
    /// - Returns: String
    /// Route id v 2.
    public func routeIdV2() -> String {
        let components = patternId.components(separatedBy: "::")
            guard let firstComponent = components.first else {
                return routeId()
            }
            return firstComponent
    }
    
    /// Route id
    /// - Returns: String
    /// Route id.
    public func routeId() -> String {
        let ids = patternId.split(separator: ":")
        var routeId = ""
        for i in 0..<ids.count - 2 {
            if routeId.count > 0 {
                routeId += ":"
            }
            routeId += ids[i]
        }
        return routeId
    }
}

